def pegar_primeiro(lista): # Criando a função
    return lista[0]  # Sempre acessa o primeiro item

brinquedos = ["ursinho", "boneca", "carrinho", "bola", "lapis", "massinha", "caderno", "aquarela", "dino"] # Lista de brinquedos para pegar o primeiro
print(pegar_primeiro(brinquedos))  # Saída: ursinho 
# Sempre rápido, não importa o tamanho da lista!
